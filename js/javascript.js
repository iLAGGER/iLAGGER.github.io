function chtoChange(combo)
{

    if(combo.value == '4')
    {
        document.getElementById('rashodniki').style.display = 'inline';
        document.getElementById('buttom').style.display = 'inline';
    }
     else
     {
        document.getElementById('rashodniki').style.display = 'none';
        document.getElementById('buttom').style.display = 'none';
     }
}

